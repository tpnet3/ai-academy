// null과 undefined 데이터 타입 예제

// undefined 예제
let undefinedVar;

console.log("=== undefined 예제 ===");
console.log("선언만 된 변수:", undefinedVar);

// null 예제
let nullVar = null;
let emptyValue = null;

console.log("\n=== null 예제 ===");
console.log("null 값이 할당된 변수:", nullVar);
console.log("빈 값으로 초기화된 변수:", emptyValue);

// null과 undefined 비교
console.log("\n=== null과 undefined 비교 ===");
console.log("null == undefined:", null == undefined);
console.log("null === undefined:", null === undefined);
console.log("typeof null:", typeof null);
console.log("typeof undefined:", typeof undefined);
