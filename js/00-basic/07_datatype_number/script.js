// 숫자(Number) 데이터 타입 예제
let age = 25;                    // 정수
let height = 175.5;              // 소수점
let temperature = -10;           // 음수
let infinity = Infinity;         // 무한대
let notANumber = NaN;           // 숫자가 아님

// 콘솔에 출력
console.log('=== 숫자 타입 예제 ===');
console.log('정수:', age);
console.log('소수점:', height);
console.log('음수:', temperature);
console.log('무한대:', infinity);
console.log('숫자가 아님:', notANumber);

// 숫자 연산
console.log('\n=== 숫자 연산 ===');
console.log('더하기:', 5 + 3);
console.log('빼기:', 5 - 3);
console.log('곱하기:', 5 * 3);
console.log('나누기:', 5 / 3);
console.log('나머지:', 5 % 3);
console.log('거듭제곱:', 5 ** 3);
