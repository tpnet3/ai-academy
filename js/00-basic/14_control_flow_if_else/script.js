// if-else 예제
let age = 20;

console.log("나이: " + age);

if (age >= 20) {
    console.log("성인입니다.");
} else if (age >= 17) {
    console.log("고등학생입니다.");
} else if (age >= 14) {
    console.log("중학생입니다.");
} else {
    console.log("초등학생 이하입니다.");
}

